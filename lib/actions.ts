export const getUserByEmail = async (email: string) => {
  try {
    const user = await prisma.user.findUnique({
      where: {
        email,
      },
    });
    return user;
  } catch (error) {
    console.log(error);
    return null;
  }
};

export const logOut = async () => {
  try {
    await signOut();
  } catch (error) {
    console.log(error);
  }
};
